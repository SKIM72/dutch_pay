create table public.settlement_members (
  id bigint generated by default as identity primary key,
  settlement_id bigint references public.settlements(id) on delete cascade,
  user_id uuid references auth.users(id) on delete cascade,
  email text,
  provider text,
  joined_at timestamptz default timezone('utc', now()) not null,
  last_read_at timestamptz default timezone('utc', now()),
  unique (settlement_id, user_id)
);

create table public.chat_messages (
