create or replace function public.is_settlement_owner(p_settlement_id bigint)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.settlements s
    where s.id = p_settlement_id
      and s.user_id = auth.uid()
      and s.deleted_at is null
  );
$$;

create or replace function public.is_settlement_member(p_settlement_id bigint)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.settlement_members sm
    join public.settlements s on s.id = sm.settlement_id
    where sm.settlement_id = p_settlement_id
      and sm.user_id = auth.uid()
      and s.deleted_at is null
  );
$$;

create or replace function public.can_access_settlement(p_settlement_id bigint)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select
    public.is_settlement_owner(p_settlement_id)
    or public.is_settlement_member(p_settlement_id);
$$;

