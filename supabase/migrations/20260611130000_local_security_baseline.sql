create or replace function public.get_public_settlement_by_invite_code(p_invite_code text)
returns jsonb
language plpgsql
stable
security definer
set search_path = ''
as $$
declare
  normalized_code text := upper(trim(coalesce(p_invite_code, '')));
  preview jsonb;
begin
  if normalized_code !~ '^[A-Z0-9]{6}$' then
    return null;
  end if;

  select jsonb_build_object(
    'id', s.id,
    'date', s.date,
    'title', s.title,
    'expenses', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'id', e.id,
          'name', e.name,
          'payer', e.payer,
          'split', e.split,
          'amount', e.amount,
          'shares', e.shares,
          'currency', e.currency,
          'created_at', e.created_at,
          'expense_date', e.expense_date,
          'settlement_id', e.settlement_id,
          'original_amount', e.original_amount
        )
        order by e.expense_date desc, e.created_at desc
      )
      from public.expenses e
      where e.settlement_id = s.id
    ), '[]'::jsonb),
    'created_at', s.created_at,
    'is_settled', s.is_settled,
    'invite_code', s.invite_code,
    'participants', s.participants,
    'base_currency', s.base_currency
  )
  into preview
  from public.settlements s
  where upper(s.invite_code) = normalized_code
    and s.deleted_at is null;

  return preview;
end;
$$;

create or replace function public.get_settlement_id_by_invite_code(p_invite_code text)
returns bigint
language sql
stable
security definer
set search_path = ''
as $$
  select s.id
  from public.settlements s
  where upper(s.invite_code) = upper(trim(coalesce(p_invite_code, '')))
    and s.deleted_at is null
  limit 1;
$$;

create or replace function public.join_settlement_by_invite_code(p_invite_code text)
returns bigint
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_uid uuid := auth.uid();
  target_settlement_id bigint;
  jwt jsonb := auth.jwt();
begin
  if current_uid is null then
    raise exception 'Authentication required.';
  end if;

  select s.id
  into target_settlement_id
  from public.settlements s
  where upper(s.invite_code) = upper(trim(coalesce(p_invite_code, '')))
    and s.deleted_at is null
  limit 1;

  if target_settlement_id is null then
    return null;
  end if;

  insert into public.settlement_members (
    settlement_id,
    user_id,
    email,
    provider
  )
  values (
    target_settlement_id,
    current_uid,
    jwt ->> 'email',
    coalesce(jwt -> 'app_metadata' ->> 'provider', 'email')
  )
  on conflict (settlement_id, user_id)
  do update set
    email = excluded.email,
    provider = excluded.provider;

  return target_settlement_id;
end;
$$;

create or replace function public.get_my_admin_pin()
returns text
language plpgsql
stable
security definer
set search_path = ''
as $$
declare
  current_uid uuid := auth.uid();
  pin text;
begin
  if current_uid is null then
    raise exception 'Authentication required.';
  end if;

  select p.admin_pin
  into pin
  from public.profiles p
  where p.user_id = current_uid;

  return pin;
end;
$$;

create or replace function public.set_my_admin_pin(p_admin_pin text)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_uid uuid := auth.uid();
begin
  if current_uid is null then
    raise exception 'Authentication required.';
  end if;

  if p_admin_pin is not null and length(p_admin_pin) > 32 then
    raise exception 'PIN is too long.';
  end if;

  update public.profiles
  set admin_pin = nullif(trim(p_admin_pin), '')
  where user_id = current_uid;

  if not found then
    raise exception 'Profile not found.';
  end if;
end;
$$;

create or replace function public.kick_member(
  p_settlement_id bigint,
  p_target_uid uuid
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
begin
  if auth.uid() is null then
    raise exception 'Authentication required.';
  end if;

  if not public.is_settlement_owner(p_settlement_id) then
    raise exception 'Only the settlement owner can remove members.';
  end if;

  if exists (
    select 1
    from public.settlements s
    where s.id = p_settlement_id
      and s.user_id = p_target_uid
  ) then
    raise exception 'The settlement owner cannot be removed.';
  end if;

  delete from public.settlement_members
  where settlement_id = p_settlement_id
    and user_id = p_target_uid;
end;
$$;

create or replace function public.delete_user()
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_uid uuid := auth.uid();
begin
  if current_uid is null then
    raise exception 'Authentication required.';
  end if;

  update public.settlements
  set user_id = null
  where user_id = current_uid;

  update public.expenses
  set user_id = null
  where user_id = current_uid;

  delete from auth.users
  where id = current_uid;
end;
$$;

